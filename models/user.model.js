import mongoose from "mongoose";

const graphQLAuthUserSchema = mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
}, {timestamps: true})

const graphQLAuthUser = mongoose.model("graphQLAuthUser", graphQLAuthUserSchema);

export default graphQLAuthUser;